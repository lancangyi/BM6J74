//----------------------------------------------------------------------------
// Special Handle
// Get 1 Bytes Embedded Controller Data, EC_DATA (R/W)
//----------------------------------------------------------------------------
BYTE OEM_GetPort68Data(void)
{

    WORD  iLOOP;
    iLOOP = WaitECDataDelay;

    do
    {
        if ( IS_BIT_SET(PM2STS,1) )
        {
            if ( PM2STS & C_D1)
            {
                return 0x00;
            }
            else
            {
                PM2Data = PM2DI;            // Load data
                return 0x01;
            }
        }

        iLOOP--;
    }
    while (iLOOP != 0 );

    return 0x00;

}
